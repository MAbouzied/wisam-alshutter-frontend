#!/usr/bin/env node
/**
 * Download portfolio gallery images from el-shutter.com/أعمالنا/
 *
 * Usage: node scripts/download-portfolio.js
 *
 * Output:
 * - /public/portfolio/portfolio-001.jpg, portfolio-002.jpg, ...
 * - src/data/portfolio.local.ts
 */

const fs = require("fs");
const path = require("path");
const { URL } = require("url");

const GALLERY_URL = "https://el-shutter.com/أعمالنا/";
const OUTPUT_DIR = path.join(process.cwd(), "public", "portfolio");
const DATA_FILE = path.join(process.cwd(), "src", "data", "portfolio.local.ts");

// Gallery image patterns (WordPress uploads, WhatsApp images, etc.)
const GALLERY_PATTERN =
  /https?:\/\/[^"'\s]+\/wp-content\/uploads\/[^"'\s]*(?:WhatsApp-Image|جدة-\d)[^"'\s]*\.(jpe?g|png|webp|gif)/gi;

// Exclude thumbnails and non-gallery images
const EXCLUDE_PATTERNS = [
  /لوجو/i,
  /cropped/i,
  /\d+_\d+-/, // e.g. 148_150
  /-\d{2,4}x\d{2,4}(?=\.\w+)/, // -300x225, -150x150 etc (thumbnails)
];

/**
 * Convert thumbnail URL to full-resolution by removing -WxH before extension
 */
function toFullResUrl(url) {
  return url.replace(/-\d{2,4}x\d{2,4}(?=\.\w+($|\?))/i, "");
}

/**
 * Check if URL should be excluded
 */
function shouldExclude(url) {
  return EXCLUDE_PATTERNS.some((re) => re.test(url));
}

/**
 * Extract unique gallery image URLs from HTML
 */
function extractImageUrls(html) {
  const seen = new Set();
  const urls = [];
  let m;

  const regex =
    /https?:\/\/[^"'\s<>]+\.(jpe?g|png|webp|gif)(?:\?[^"'\s<>]*)?/gi;

  while ((m = regex.exec(html)) !== null) {
    let url = m[0];
    // Only wp-content/uploads gallery images
    if (!url.includes("/wp-content/uploads/")) continue;
    if (!url.includes("WhatsApp-Image") && !/جدة-\d/.test(url)) continue;
    if (shouldExclude(url)) continue;

    url = toFullResUrl(url);
    // Normalize: ensure https
    url = url.replace(/^http:\/\//, "https://");

    if (!seen.has(url)) {
      seen.add(url);
      urls.push(url);
    }
  }

  // Also check src="..." and srcset
  const srcRegex = /src="(https?:\/\/[^"]+)"/g;
  while ((m = srcRegex.exec(html)) !== null) {
    let url = m[1].replace(/&amp;/g, "&");
    if (!url.includes("/wp-content/uploads/")) continue;
    if (!url.includes("WhatsApp-Image") && !/جدة-\d/.test(url)) continue;
    if (shouldExclude(url)) continue;

    url = toFullResUrl(url);
    url = url.replace(/^http:\/\//, "https://");

    if (!seen.has(url)) {
      seen.add(url);
      urls.push(url);
    }
  }

  return urls;
}

/**
 * Get file extension from URL
 */
function getExtension(url) {
  const match = url.match(/\.(jpe?g|png|webp|gif)(?:\?|$)/i);
  return match ? (match[1].toLowerCase() === "jpeg" ? "jpg" : match[1].toLowerCase()) : "jpg";
}

/**
 * Download a single image
 */
async function downloadImage(url, filepath) {
  const res = await fetch(url, {
    headers: { "User-Agent": "Mozilla/5.0 (compatible; PortfolioDownloader/1.0)" },
    redirect: "follow",
  });
  if (!res.ok) throw new Error(`HTTP ${res.status}`);
  const buf = Buffer.from(await res.arrayBuffer());
  fs.writeFileSync(filepath, buf);
}

async function main() {
  console.log("Portfolio image downloader");
  console.log("Source:", GALLERY_URL);
  console.log("Output:", OUTPUT_DIR);
  console.log("");

  // Ensure output dir exists
  fs.mkdirSync(OUTPUT_DIR, { recursive: true });
  fs.mkdirSync(path.dirname(DATA_FILE), { recursive: true });

  // Fetch HTML
  console.log("Fetching page...");
  const res = await fetch(GALLERY_URL, {
    headers: { "User-Agent": "Mozilla/5.0 (compatible; PortfolioDownloader/1.0)" },
  });
  if (!res.ok) throw new Error(`Failed to fetch: ${res.status}`);
  const html = await res.text();

  // Extract URLs
  const urls = extractImageUrls(html);
  const unique = [...new Set(urls)];
  console.log(`Found ${unique.length} unique gallery images`);
  if (unique.length === 0) {
    console.log("No gallery images found. Creating empty portfolio.local.ts.");
    fs.writeFileSync(
      DATA_FILE,
      `/**
 * Portfolio data - generated by scripts/download-portfolio.js
 * Run: node scripts/download-portfolio.js
 */

export type PortfolioCategoryKey = "shutter" | "aluminum_windows" | "aluminum_doors";
export type PortfolioCity = "both" | "جدة" | "الرياض";

export interface PortfolioLocalItem {
  id: string;
  src: string;
  titleAr: string;
  category: PortfolioCategoryKey;
  city: PortfolioCity;
}

export const portfolioLocal: PortfolioLocalItem[] = [];
`
    );
    console.log(`Generated: ${DATA_FILE}`);
    process.exit(0);
  }

  let downloaded = 0;
  let skipped = 0;
  const results = [];

  for (let i = 0; i < unique.length; i++) {
    const url = unique[i];
    const num = String(i + 1).padStart(3, "0");
    const ext = getExtension(url);
    const filename = `portfolio-${num}.${ext}`;
    const filepath = path.join(OUTPUT_DIR, filename);

    if (fs.existsSync(filepath)) {
      console.log(`  [${i + 1}/${unique.length}] Skipped (exists): ${filename}`);
      skipped++;
      results.push({
        id: String(i + 1),
        src: `/portfolio/${filename}`,
        titleAr: `عمل ${i + 1}`,
        category: "shutter",
        city: "both",
      });
      continue;
    }

    try {
      await downloadImage(url, filepath);
      console.log(`  [${i + 1}/${unique.length}] Downloaded: ${filename}`);
      downloaded++;
      results.push({
        id: String(i + 1),
        src: `/portfolio/${filename}`,
        titleAr: `عمل ${i + 1}`,
        category: "shutter",
        city: "both",
      });
    } catch (err) {
      console.error(`  [${i + 1}/${unique.length}] Failed ${filename}:`, err.message);
    }
  }

  console.log("");
  console.log(`Done: ${downloaded} downloaded, ${skipped} skipped, ${unique.length} total`);

  // Generate portfolio.local.ts
  const itemsStr = results
    .map(
      (r) =>
        `  { id: "${r.id}", src: "${r.src}", titleAr: "${r.titleAr}", category: "${r.category}", city: "${r.city}" }`
    )
    .join(",\n");

  const tsContent = `/**
 * Portfolio data - generated by scripts/download-portfolio.js
 * Titles and categories can be edited manually. Re-run script only to add new images.
 */

export type PortfolioCategoryKey = "shutter" | "aluminum_windows" | "aluminum_doors";
export type PortfolioCity = "both" | "جدة" | "الرياض";

export interface PortfolioLocalItem {
  id: string;
  src: string;
  titleAr: string;
  category: PortfolioCategoryKey;
  city: PortfolioCity;
}

export const portfolioLocal: PortfolioLocalItem[] = [\n${itemsStr}\n];
`;

  fs.writeFileSync(DATA_FILE, tsContent);
  console.log(`Generated: ${DATA_FILE}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
